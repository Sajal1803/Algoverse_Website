            <h1>What is CN ?</h1><br>A computer network is a collection of computers or devices connected to share resources. Any device which can share or receive the data is called a Node. Through which the information or data propagate is known as channels, It can be guided or unguided. <br> <br>
            In this Computer network tutorial, you'll learn basic to advanced concepts like the Basics of computer networks, data link layer, network layer, network security and cryptography, compression techniques, etc. <br> <br>

            <h2>Application of CN</h2> <br><br>
            Some of the applications of Computer Networking are discussed below: <br><br>

            1. Resource Sharing: Resource Sharing is one of the important applications of Computer Networking. You can share a single software among Multiple users. We can also share Hardware Devices via this technique.<br>
            2. Communication: Communication Medium means various ways through which we can communicate like Email Calls, broadcasts, etc.<br>
            3. Home Applications: Home Applications are an important application of Computer Networking. Examples are User-to-User Communication, Entertainment, E-Commerce, etc. <br>
            4. Business Applications: Business Application is also an important application of Computer Networking. Almost all companies are doing business online with the help of Computer Networking.<br>
            5. Social Media: Social Media is one of the recent and widely used applications of Computer Networking. It helps people in getting news, feed, current trending topics of all types, etc.<br>
            6. Access to Remote Information: Computer Networking helps in accessing remote information from the end-users. For Example. details required for train tickets, plane tickets, etc. <br>
            7. Cloud Computing: Computer Networking has facilitated the development of cloud computing, which allows users to store and access data and applications from remote servers via the internet.<br>
            8. Virtualization: Networking technologies also enable virtualization, which allows multiple virtual machines to run on a single physical machine, improving resource utilization and reducing costs.<br>
            9. Online Education: Computer Networking has revolutionized education by providing online learning platforms, distance education, and virtual classrooms.<br>
            10. Remote Working: Networking technologies have enabled remote working, allowing employees to work from home or other locations outside of the office. This has become especially important during the COVID-19 pandemic.<br>
            11. E-commerce: Computer Networking has facilitated the growth of e-commerce by allowing businesses to sell their products and services online and reach a global market.<br>
            12. Telemedicine: Networking technologies have also enabled telemedicine, allowing doctors and healthcare providers to provide remote medical consultations and diagnosis to patients in remote locations.<br>
            13. Real-time Collaboration: Networking technologies enable real-time collaboration, allowing people to work together on projects and share information in real-time, no matter where they are located. <br><br><br>

            <h3>Advantages Of CN:</h3> <br><br>
            1. Central Storage of Data: Files can be stored on a central node (the file server) that can be shared and made available to every user in an organization. <br>
            2. Connectivity: There is a negligible range of abilities required to connect to a modern computer network. The effortlessness of joining makes it workable for even youthful kids to start exploring the data. <br>
            3. Faster Problem-solving: Since an extensive procedure is disintegrated into a few littler procedures and each is taken care of by all the associated gadgets, an explicit issue can be settled in lesser time. <br>
            4. Reliability: Reliability implies backing up information. Due to some reason equipment crashes, and so on, the information gets undermined or inaccessible on one PC, and another duplicate of similar information is accessible on another workstation for future use, which prompts smooth working and further handling without interruption. <br>
            5. Flexible: This innovation is known to be truly adaptable, as it offers clients the chance to investigate everything about fundamental things, for example, programming without influencing their usefulness. <br>
            6. Security through Authorization: Security and protection of information are additionally settled through the system. As just the system clients are approved to get to specific records or applications, no other individual can crack the protection or security of information.<br>
            7. Storage capacity: Since you will share data, records, and assets with other individuals, you need to guarantee all information and substance are legitimately put away in the framework. With this systems administration innovation, you can do most of this with no issue, while having all the space you require for capacity. <br><br>

            <h3>Disadvantages of CN</h3> <br><br>
            1. Expensive: Execution of the network can be expensive in the case of an initial setup, as the wires and the cost of the cable are high and sometimes equipment is also costly.<br>
            2. Virus and Malware: Computer Networking can lead to the spreading of viruses to another computer through the network.
            Management of the network: Management of the Network is quite difficult as it requires skilled persons to handle that large network. It requires training of people who are employed in this work.<br> 
            3. Loss of Information: In case of a crash of the Computer Network, it can lead to the loss of information or not being able to access information for some time. <br>
            4. The system can be Hacked: In the case of Wide Area Networks(WAN), there is a threat of Hacking of the System. Some security features should be added to prevent such things. <br><br>

            <h3>What are the types of computer network?</h3> <br><br>
            PAN(Personal Area Network): It is the network connecting computer devices for personal use within a range of 10 meters. <br>
            LAN(Local Area Network): It is a collection of computers connected to each other in a small area for example school, office, or building.<br>
            WAN(Wide Area Network): A Wide Area Network is a large area than the LAN. It is spread across the states or countries.<br>
            MAN(Metropolitan Area Network): A Metropolitan area network is the collection of interconnected Local Area Networks.<br><br>

            <h3>What is the network topology?</h3> <br><br>
            Network topology is the physical design of the network, It represents the connectivity between the devices, cables, computers, etc.<br><br>

            <h3> What are different types of network topology?</h3> <br><br>
